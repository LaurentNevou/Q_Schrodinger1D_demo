Vb=0.3;                     % potential barrier [eV]

zt = [10 10 5 3  10]*1E-9;  % thickness of each layer [nm]
Vt = [1 0 1 0 1]*Vb;        % barrier height of each layer [eV]

%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%% Discretisation %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% here, I descretize the grid z and the potential V0

z(1)=0;  V0(1)=Vt(1);

for i=1:length(zt)
    zv = (z(end)+dz):dz:(z(end) + zt(i)); 
    z  = [ z  zv ];
    V0 = [ V0   (zv*0+1) * Vt(i)  ];
end

